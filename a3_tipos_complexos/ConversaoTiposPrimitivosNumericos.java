package a3_tipos_complexos;

public class ConversaoTiposPrimitivosNumericos {
  public static void main(String[] args) {
    
    double a = 1;
    System.out.println(a);

    float b = (float) 1.0;
    System.out.println(b);

    int c = 130;
    byte d = (byte) c;
    System.out.println(d);

    double e = 1.999999;
    int f = (int) e;
    System.out.println(f);

  }
}
